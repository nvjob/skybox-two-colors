using UnityEngine;
using UnityEngine.UI;


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


public class NVJOBUI : MonoBehaviour
{
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////


    void Awake()
    {
        //---------------------------------

        transform.Find("Panel/logo").GetComponent<Button>().onClick.AddListener(Logo);
        transform.Find("Panel/donation").GetComponent<Button>().onClick.AddListener(Donation);

        //---------------------------------
    }
    

    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    

    void Logo()
    {
        //---------------------------------

        Application.OpenURL("https://nvjob.github.io/unity/skybox-two-colors");

        //---------------------------------
    }


    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    

    void Donation()
    {
        //---------------------------------

        Application.OpenURL("https://nvjob.github.io/donate");

        //---------------------------------
    }


    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}
